class Command(BaseCommand):
    help = "Демо-наполнение базы знаний для kb_search"

    def add_arguments(self, parser):
        parser.add_argument("--reset", action="store_true",
                            help="Удалить ранее созданные seed-записи")

    def handle(self, *args, **options):
        from assistant.models import KnowledgeChunk

        if options["reset"]:
            n = KnowledgeChunk.objects.filter(metadata__seed=SEED_TAG).delete()
            self.stdout.write(self.style.SUCCESS(f"Удалено: {n}"))
            return

        created = 0
        for c in CHUNKS:
            obj, was_created = KnowledgeChunk.objects.update_or_create(
                source_type=c["source_type"],
                source_id=c["source_id"],
                defaults={
                    "title": c["title"],
                    "content": c["content"],
                    "metadata": {"seed": SEED_TAG},
                    "language": "ru",
                    "is_active": True,
                },
            )
            if was_created:
                created += 1

        self.stdout.write(self.style.SUCCESS(
            f"✓ База знаний: {KnowledgeChunk.objects.filter(is_active=True).count()} записей "
            f"(создано в этом запуске: {created})."
        ))
