def get_system_prompt(role: str, context_chunks=None) -> str:
    """Build full system prompt with role + RAG context."""
    prompt = BASE_SYSTEM_PROMPT
    role_prompt = ROLE_PROMPTS.get(role, ROLE_PROMPTS["buyer"])
    prompt += "\n\n" + role_prompt

    if context_chunks:
        prompt += "\n\n--- КОНТЕКСТ ИЗ БАЗЫ ДАННЫХ ---\n"
        for i, chunk in enumerate(context_chunks, 1):
            prompt += f"\n[Источник {i}: {chunk.get_source_type_display()} — {chunk.title}]\n"
            prompt += chunk.content + "\n"
            if chunk.metadata:
                meta_parts = [f"{k}: {v}" for k, v in chunk.metadata.items() if v is not None]
                if meta_parts:
                    prompt += f"Метаданные: {', '.join(meta_parts)}\n"
        prompt += "\n--- КОНЕЦ КОНТЕКСТА ---\n"
        prompt += "\nОтвечай строго на основе предоставленного контекста. Цитируй конкретные источники по номеру."
    else:
        prompt += "\n\nКонтекст не найден. Если не можешь ответить без данных — честно скажи об этом."

    return prompt
